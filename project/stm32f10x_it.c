/*******************************************************************************
* Function Name  : USART1_IRQHandler
* Description    : This function handles USART1 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void USART1_IRQHandler(void)
{
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)
	{
		/* 清除USART1接收中断标志 */
		USART_ClearITPendingBit(USART1, USART_IT_RXNE);

		if(UART_DataReceive==0)
		{
			/* 从UART1接收寄存器读取一个字节的数据 */
			UART_Rx_Buffer[RxCounter] = USART_ReceiveData(USART1);

			/* 检查是否是数据包的头 */
			if(RxCounter==1)
			{
				if((UART_Rx_Buffer[0]!=0xaa) || ( UART_Rx_Buffer[1]!=0x55))
				{
					UART_Rx_Buffer[0]=UART_Rx_Buffer[1];

					RxCounter--;
				}
			}
			/* 检查是否接收完整的数据包 */
			else if(RxCounter>=4)
			{
				if(RxCounter==UART_Rx_Buffer[2]+2)
					UART_DataReceive=1;
			}

			RxCounter++;
		}
	}

	if(USART_GetITStatus(USART1, USART_IT_TC) != RESET)
	{
		/* 清除USART1发送中断标志 */
		USART_ClearITPendingBit(USART1, USART_IT_TC);

		if(TxCounter!=TxIndex)			/* 检查发送是否完成 */
		{
			USART_SendData(USART1, UART_Tx_Buffer[TxIndex]);
			TxIndex++;
		}
	}
}

